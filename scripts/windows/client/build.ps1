var
  ConfigPage: TInputQueryWizardPage;

// Create the Custom Configuration Page
procedure InitializeWizard;
begin
  ConfigPage := CreateInputQueryPage(wpWelcome,
    'Connection Configuration', 'Server Details',
    'Please enter the connection details for your Remote FS.');

  ConfigPage.Add('Server URL:', False);
  
  // Set Defaults
  ConfigPage.Values[0] := 'http://localhost:8080';
end;

// Build the Command Line Arguments
function GetParams(Param: String): String;
begin
  // format: run --server-url "URL"
  Result := 'run --server-url "' + ConfigPage.Values[0];
end;

// Check for WinFsp
function NeedWinFsp: Boolean;
begin
  Result := not FileExists(ExpandConstant('{commonpf64}\WinFsp\bin\winfsp-x64.dll'));
end;

// Check Env Path System
function NeedsWinFspPath: Boolean;
var
  OrigPath: String;
begin
  // Try to read the SYSTEM 'Path' variable
  if RegQueryStringValue(HKLM, 'SYSTEM\CurrentControlSet\Control\Session Manager\Environment', 'Path', OrigPath) then
    // It exists, so check if our folder is already in it
    Result := Pos('WinFsp\bin', OrigPath) = 0
  else
    Result := True;
end;

function NeedsWinFspPathUser: Boolean;
var
  OrigPath: String;
begin
  // Try to read the CURRENT USER 'Path' variable
  if RegQueryStringValue(HKCU, 'Environment', 'Path', OrigPath) then
    // It exists, so check if our folder is already in it
    Result := Pos('WinFsp\bin', OrigPath) = 0
  else
    Result := True
end;

