    New-Item -ItemType Directory -Force -Path $BuildDir | Out-Null 
}

# Copy files to Build Directory
Write-Host "[INFO] Gathering Files..." -ForegroundColor Cyan
$ExeSource =  [System.IO.Path]::GetFullPath((Join-Path $ProjectRoot "target\release\client.exe"))
$ExeDest =  [System.IO.Path]::GetFullPath((Join-Path $BuildDir $AppExeName))
Copy-Item -Path $ExeSource -Destination $ExeDest -Force

# Define WinFsp Repo
$Repo = "winfsp/winfsp"
# Get the Latest Release Info from GitHub API
$ApiUrl = "https://api.github.com/repos/$Repo/releases/latest"
$LatestRelease = Invoke-RestMethod -Uri $ApiUrl
# Find the asset you want (e.g., the MSI or your EXE)
# This filters the assets list to find one ending in .msi
$WinFspAsset = $LatestRelease.assets | Where-Object { $_.name -like "*.msi" }

$WinFspUrl = $WinFspAsset | Select-Object -ExpandProperty browser_download_url
$MsiName = $WinFspAsset.name
$MsiDest = Join-Path $BuildDir $MsiName

# Download WinFsp MSI if not present
if (-not (Test-Path $MsiDest)) {
    # Download it
    Write-Host "[INFO] Downloading WinFsp MSI..." -ForegroundColor Yellow
    Invoke-WebRequest -Uri $WinFspUrl -OutFile $MsiDest
    Write-Host "Downloaded version: $($LatestRelease.tag_name)"
} else {
    Write-Host "[INFO] WinFsp MSI already exists (skipping download)." -ForegroundColor Gray
}

# Copy Icon File
